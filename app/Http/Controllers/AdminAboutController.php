     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        //

        $request->validate([
            'facebook' => 'url|nullable',
            'github' => 'url|nullable',
            'twitter' => 'url|nullable',
        ]);

        $about = About::findOrFail($id);
        $inputs = $request->all();

        $about->update($inputs);
        session()->flash('about_update_msg', 'Updated');
        return redirect()->back();
    }

